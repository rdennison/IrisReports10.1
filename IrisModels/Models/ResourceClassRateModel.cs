    //[IrisGrid("~/API/ResourceClassRate/Read", "~/API/ResourceClassRate/Create", "~/API/ResourceClassRate/Update", "~/API/ResourceClassRate/Destroy")]
    public sealed class ResourceClassRateModel : ModelBase
    {
